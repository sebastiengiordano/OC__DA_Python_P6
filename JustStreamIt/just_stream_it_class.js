class Film {
    constructor(
        image_url,
        title,
        genres,
        date_published,
        rated,
        imdb_score,
        directors,
        actors,
        duration,
        countries,
        budget,
        long_description) {
      this.image_url = image_url;
      this.title = title;
      this.genres = genres;
      this.date_published = date_published;
      this.rated = rated;
      this.imdb_score = imdb_score;
      this.directors = directors;
      this.actors = actors;
      this.duration = duration;
      this.countries = countries;
      this.budget = budget;
      this.long_description = long_description;
    }
}
